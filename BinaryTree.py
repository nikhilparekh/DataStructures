class Node(object):
    def __init__(self,data):
        self.data = data
        self.left = None
        self.right = None
        

class BinaryTree(object):
    def __init(self,root):
        self.root = Node(root)

#setting up tree

tree = BinaryTree(1)
tree.root.left = Node(2)
tree.root.right = Node(3)
    

